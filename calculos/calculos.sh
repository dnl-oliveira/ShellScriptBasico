#!/bin/bash

#Um simples programa com alguns calculos

PRIMEIRO=4
SEGUNDO=2

#Adicionar
echo "Adicionar:"
echo $((PRIMEIRO+SEGUNDO))

#Subtrair
echo "Subtrair:"
echo $((PRIMEIRO-SEGUNDO))

#Multiplicar
echo "Multiplicar:"
echo $((PRIMEIRO*SEGUNDO))

#Dividir
echo "Dividir:"
echo $((PRIMEIRO/SEGUNDO))
